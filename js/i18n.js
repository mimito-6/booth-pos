  let locale = "zh-Hant";

  function setLocale(l) {
    if (DICT[l]) locale = l;
  }
  function getLocale() {
    return locale;
  }
  function detect() {
    const n = (navigator.language || "zh-Hant").toLowerCase();
    if (n.startsWith("ja")) return "ja";
    if (n.startsWith("ko")) return "ko";
    if (n.startsWith("zh")) return "zh-Hant";
    if (n.startsWith("en")) return "en";
    return "zh-Hant";
  }
  function t(key, params) {
    let s = (DICT[locale] && DICT[locale][key]) || DICT["zh-Hant"][key] || key;
    if (params) {
      for (const k in params) s = s.replace(new RegExp("\\{" + k + "\\}", "g"), params[k]);
    }
    return s;
  }

  OB.i18n = { DICT, setLocale, getLocale, detect, t, locales: Object.keys(DICT) };
